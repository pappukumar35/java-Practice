package operator;

import java.util.*;

public class Arithmetic {
     public static void main(String[] args) {
          int p = 10;
          int q = 20;
          System.out.println(p + q);

          //
          int s = 10;
          int r = 15;
          System.out.println(s + r);
          System.out.println(s - r);
          System.out.println(s * r);
          System.out.println(s / r);
          System.out.println(s % r);
          // increment
          System.out.println(r++);
          System.out.println(--p);

     }
}
